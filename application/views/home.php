				<h2 class="align-center color-white">¿Qué es?</h2>
				<p class="m-paragraph color-white">
					Ciudad Equitativa, Ciudad Inclusiva (CECI) es una plataforma creada con el objetivo de fortalecer y ampliar capacidades técnicas de las administraciones locales para la planeación, gestión y ejecución de proyectos de movilidad urbana sustentable desde una perspectiva que integre los criterios de equidad, accesibilidad, seguridad y sostenibilidad. Todo ello para dar cumplimiento a los objetivos del Programa Nacional de Desarrollo Urbano 2014 – 2018 (PNDU). También es una guía para grupos y organizaciones ciudadanas que busquen impulsar proyectos de movilidad urbana.
				</p>
